/* 세부유형 텍스트에서 처방 찾기 (키워드 매칭) */
function findRx(typeText){
  if(!typeText) return null;
  const t = typeText.toLowerCase();
  for(const rx of PRESCRIPTION){
    if(rx.kw.some(k => t.includes(k.toLowerCase()))) return rx;
  }
  return null;
}

